public class CheckAlpha {
    public static void main(String[] args) {
        char ch = 'u';
        if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' )
            System.out.println(ch + " is an vowel");
        else
            System.out.println(ch + " is an consonant");
    }

}